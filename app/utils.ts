export function isInTampere(lat: number, lon: number) {
  const north = 62.5;
  const south = 61.0;
  const west = 24.5;
  const east = 22.5;

  if (lat >= south && lat <= north && lon >= west && lon <= east) {
    return true;
  } else {
    return false;
  }
}

export function timeDifference(dateString: string) {
  const now = new Date();
  const targetDate = new Date(dateString);

  if (isNaN(targetDate.getTime())) {
    return "Invalid date";
  }

  const diffMs = targetDate.getTime() - now.getTime();
  const diffMinutes = Math.floor(diffMs / 60000);

  switch (true) {
    case diffMinutes <= 0:
      return "Now";
    case diffMinutes <= 40:
      return `${diffMinutes}min`;
    default:
      const localHours = targetDate.getHours();
      const localMinutes = targetDate.getMinutes();
      return `${localHours}:${localMinutes < 10 ? "0" : ""}${localMinutes}`;
  }
}

export function timeToMinutes(timeStr: string) {
  const [hours, minutes] = timeStr.split(":").map(Number);
  return hours * 60 + minutes;
}

export function formatTime(dateTime: string) {
  const date = new Date(dateTime);
  const hours = String(date.getHours()).padStart(2, "0");
  const minutes = String(date.getMinutes()).padStart(2, "0");
  return `${hours}:${minutes}`;
}
